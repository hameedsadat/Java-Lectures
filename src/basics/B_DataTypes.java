package basics;

public class B_DataTypes {

	public static void main(String[] args) {
		
		
		// Java identifiers
		
		int age = 20;
		
		int a = 30; // here we dont know what a is while age makes things clear enough
		
		/*
		 * The general rules for naming variables are: • Names can contain letters,
		 * digits, underscores, and dollar signs • Names must begin with a letter •
		 * Names should start with a lowercase letter and it cannot contain whitespace •
		 * Names can also begin with $ and _ (but we will not use it in this tutorial) •
		 * Names are case sensitive ("myVar" and "myvar" are different variables) •
		 * Reserved words (like Java keywords, such as int or boolean) cannot be used as
		 * names
		 */
		
		// Java Data Types
		
		int myNum = 4;
		float myfloatNum = 3.4f;
		char grade = 'A';
		double d = 1.1;
		boolean fa = true;
		String myText = "hello";
		
		// integer types: 
		byte myNu = 127;
		short myShort = 32767;
		int phoneN =  2147483647;
		long myLongN = 9223372036854775807L;
		
		// Floating Point Types: float and double
		
		float myFl = 3.3f;
		double myD = 3.4;
		double myDa = 44.0d;
		
		// Boolean type : 
		boolean isJavaFun = true;
		boolean isFishTasty = false;
		
		//Char data types
		
		char myGrade = 'A';
		
		String greetings = "hi";
		
		
	}

}
